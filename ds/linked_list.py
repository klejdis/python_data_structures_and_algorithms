"""
Linked List Data Structure
are linear data structures where the elements are not stored in contiguous
locations and every element is a separate object with a data part and address part.
The elements are linked using pointers and addresses.
Each element is known as a node. Due to the dynamicity and ease of insertions and deletions,
they are preferred over the arrays. It also has few disadvantages
like the nodes cannot be accessed directly instead we need to start from the head and follow through the
link to reach to a node we wish to access.
"""


class Node:
    def __init__(self, data):
        self.data = data
        self.next = None


class LinkedList:
    def __init__(self):
        self.head = None

    def print_list(self):
        cur_node = self.head
        while cur_node:
            print(cur_node.data)
            cur_node = cur_node.next

    def append(self, data):
        new_node = Node(data)
        if self.head is None:
            self.head = new_node
            return
        last_node = self.head
        while last_node.next:
            last_node = last_node.next
        last_node.next = new_node

    def prepend(self, data):
        new_node = Node(data)
        new_node.next = self.head
        self.head = new_node

    def insert_after_node(self, prev_node, data):
        if not prev_node:
            print("Previous node is not in the list")
            return
        new_node = Node(data)
        new_node.next = prev_node.next
        prev_node.next = new_node

    def delete_node(self, key):
        cur_node = self.head
        if cur_node and cur_node.data == key:
            self.head = cur_node.next
            cur_node = None
            return
        prev = None
        while cur_node and cur_node.data != key:
            prev = cur_node
            cur_node = cur_node.next
        if cur_node is None:
            return
        prev.next = cur_node.next
        cur_node = None

    def delete_node_at_pos(self, pos):
        cur_node = self.head
        if pos == 0:
            self.head = cur_node.next
            cur_node = None
            return
        prev = None
        count = 1
        while cur_node and count != pos:
            prev = cur_node
            cur_node = cur_node.next
            count += 1
        if cur_node is None:
            return
        prev.next = cur_node.next
        cur_node = None

    def len_iterative(self):
        count = 0
        cur_node = self.head
        while cur_node:
            count += 1
            cur_node = cur_node.next
        return count

    def len_recursive(self, node):
        if node is None:
            return 0
        return 1 + self.len_recursive(node.next)

    def get(self, index):
        if index >= self.len_iterative():
            print("Index out of range")
            return
        count = 0
        cur_node = self.head
        while cur_node:
            if count == index:
                return cur_node.data
            count += 1
            cur_node = cur_node.next

    def __str__(self):
        cur_node = self.head
        result = ''
        while cur_node:
            result += str(cur_node.data) + ' -> '
            cur_node = cur_node.next